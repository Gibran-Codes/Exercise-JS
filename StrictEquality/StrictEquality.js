// === Strict equality operator
// !== Strict equality operator
// (Checking if the data type and value are the same)



const PI1 = "3.14";

if (PI1 === 3.14) {
    console.log("This is a PI");
}
else {
    console.log("This is NOT a PI");
}

const PI2 = "3.14";

if (PI1 !== 3.14) {
    console.log("This is Not a PI");
}
else {
    console.log("This is a PI");
}